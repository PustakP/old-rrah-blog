    $(document).ready(function(){
    /*
    Create a script that assigns random position values (-5% - 99%) 
    and a random lifetime(1-3s) to a dynamically generated element 
    The elements will be fixed <p>'s containing the string "ALL RESET".
    They will also have a random size ranging from 0.5 to 2em and propagate randomly.
    On the last second of their lifetime they will fade in opacity from 1 to 0 then be deleted
    
    With this; the design goal of "raindrop text" will be achieved. 
    */

    // Shorthand for the container
    var $cont = $("#container");
    // Here we create an array.
    var ar_array = [];
            
    // Here we write the function that style the elements with randomized values.
    var styleThem = function(elem){
        
        myRand = function(neg, pos){
            num = Math.floor(Math.random()* pos) + neg; // this will get a number between 1 and 99;
            num *= Math.floor(Math.random()*2) === 1 ? 1 : -1; // this will add minus sign in 50% of cases
            return num;
        };    
        
        // Position value generator
        x_val = Math.floor((Math.random()* 99)+ -2);
            x_val = x_val.toString() + '%';
        y_val = Math.floor((Math.random()* 99)+ -2);
            y_val = y_val.toString() + '%';    
        // font size generator    
        font_size = Math.floor((Math.random()* 64)+ 5);
            font_size = font_size.toString() + 'px';    
        // text shadow generator     
        text_shadow_x = myRand(-3, 3);
        text_shadow_y = myRand(-3, 3);
        text_shadow = text_shadow_x.toString() + "px " + text_shadow_y.toString() + "px " + "4px" + "#FF5500"; 
        // Z- index generator
        z_index = Math.floor((Math.random()* 100)+ 1);
            z_index = z_index.toString();
        // Opacity generator
        opa_city = Math.random();
        
        // Value to attribute conversion for positions
        return elem.css("top" , y_val)
            .css("left", x_val)
            // Value to attribute conversion for text
            .css("fontSize", font_size)
            .css("textShadow" , text_shadow)
            // Value to attribute conversion for z index
            .css("zIndex" , z_index)
            .css("opacity" , opa_city);
    };
   
    // ...and add 100 separate elements to it.
    for ( x = 0; x < 100; x++){
        ar_array[x] = $('<p>LET GO</p>')
        // Adds the desired text to the elements then adds some universal styling.
           .css({
        // Values generated by the styling function will be empty strings
                position: 'absolute',
                top: '',
                left: '',
                fontSize: '',
                zIndex: '',
                textShadow: '',
                opacity: '',
                color: 'red',
                margin: 'auto',
                whiteSpace: 'nowrap'
            });
    };

    //...then we run the function that gives each element it's styling values
    for (x = 0; x < ar_array.length; x++){
        ar_array[x] = styleThem( ar_array[x] );
    }
    
    //...and finally we add these elements to the document in #container
    for (x = 0; x < ar_array.length; x++){
        $cont.append( ar_array[x] );
    };
   
    
    // Here we create a function that will "revive" the elements when they reach opacity 0.
    var dropping = function(){
               
        for (x = 0; x < ar_array.length; x++){
            ar_array[x] = styleThem( ar_array[x] );
        }
    };
    
    setInterval( dropping , 250);
    
    
    // End of document
    });